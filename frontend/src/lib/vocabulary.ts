export const getPredictions = (input: string): string[] => {
    if (!input) return [];
    
    // Normalize input to katakana for matching if user types hiragana?
    // User requested "input a Katakana", so assuming they might type katakana directly or we convert.
    // Let's support matching by the input as-is or if it were hiragana converted to katakana.
    // But typically IME handles conversion. If the user input is in the textarea, it could be hiragana or katakana or romaji.
    // The requirement says: "display prediction after user inputs a katakana" (or "as user inputs").
    // Let's keep it simple: matches startsWith.
    
    return KATAKANA_VOCABULARY.filter(word => word.startsWith(input));
};
